# Main detection function
# ---------------------------------------------------------------------------

def detect_ai_content(text, verify_entities=True):
    """
    Analyze text for signs of AI generation.

    Args:
        text: The text to analyze.
        verify_entities: If True, search the web to verify whether
            institutions/journals/people mentioned in the text actually
            exist. This adds ~2-5 seconds but is very powerful.

    Returns:
        dict: {
            'is_ai_generated': bool,
            'ai_probability': float (0.0 - 1.0),
            'signals': list of (signal_name, score, description),
            'fictional_entities': list of entity names that couldn't be found,
            'verified_entities': list of entity names that were found,
        }
    """
    # Robustly normalize text before any heuristic checks
    text = normalize_text(text)
    if not text or len(text) < 80:
        return {
            'is_ai_generated': False,
            'ai_probability': 0.0,
            'signals': [],
            'fictional_entities': [],
            'verified_entities': [],
        }

    text_lower = normalize_for_heuristics(text)
    signals = []
    weighted_scores = []

    # --- Signal 1: Explicit markers (weight: very high) ---
    explicit_markers = _check_explicit_markers(text_lower)
    if explicit_markers:
        score = min(len(explicit_markers) * 0.5, 1.0)
        signals.append(('explicit_ai_markers', score,
                        f'Contains explicit AI/fictional markers: {", ".join(explicit_markers)}'))
        weighted_scores.append(('explicit_ai_markers', score, 3.0))  # weight 3.0

    # --- Signal 2: Sentence length uniformity (weight: medium) ---
    uniformity_score = _analyze_sentence_uniformity(text)
    if uniformity_score > 0.0:
        signals.append(('sentence_uniformity', uniformity_score,
                        f'Sentence lengths are unusually uniform (AI-typical pattern)'))
        weighted_scores.append(('sentence_uniformity', uniformity_score, 1.0))

    # --- Signal 3: Transition phrase density (weight: medium) ---
    transition_score, transition_count = _analyze_transition_density(text_lower)
    if transition_score > 0.0:
        signals.append(('transition_density', transition_score,
                        f'High density of AI-typical transition phrases ({transition_count} found)'))
        weighted_scores.append(('transition_density', transition_score, 1.2))

    # --- Signal 4: Hedging balance (weight: medium-high) ---
    hedging_score = _analyze_hedging_balance(text)
    if hedging_score > 0.0:
        signals.append(('hedging_balance', hedging_score,
                        f'Formulaic balanced hedging pattern detected (AI signature)'))
        weighted_scores.append(('hedging_balance', hedging_score, 1.5))

    # --- Signal 5: Passive voice / reported speech (weight: low-medium) ---
    passive_score, passive_count = _analyze_passive_voice(text)
    if passive_score > 0.0:
        signals.append(('passive_voice', passive_score,
                        f'High passive voice / reported speech density ({passive_count} instances)'))
        weighted_scores.append(('passive_voice', passive_score, 0.8))

    # --- Signal 6: Adverb density (weight: low) ---
    adverb_score, adverb_count = _analyze_adverb_density(text_lower)
    if adverb_score > 0.0:
        signals.append(('adverb_density', adverb_score,
                        f'Elevated AI-typical adverb usage ({adverb_count} instances)'))
        weighted_scores.append(('adverb_density', adverb_score, 0.7))

    # --- Signal 7: Paragraph regularity (weight: low-medium) ---
    para_score = _analyze_paragraph_regularity(text)
    if para_score > 0.0:
        signals.append(('paragraph_regularity', para_score,
                        f'Paragraph lengths are unusually regular (AI-typical)'))
        weighted_scores.append(('paragraph_regularity', para_score, 0.8))

    # --- Signal 8: Fictional entity verification (weight: VERY high) ---
    fictional_entities = []
    verified_entities = []

    if verify_entities:
        fictional_entities, verified_entities = _verify_entities_exist(text)
        if fictional_entities:
            entity_score = min(len(fictional_entities) * 0.5, 1.0)
            signals.append(('fictional_entities', entity_score,
                            f'Unverifiable entities found: {", ".join(fictional_entities)}'))
            weighted_scores.append(('fictional_entities', entity_score, 4.0))  # highest weight

    # Also check if "fictional" is literally used as a qualifier
    fictional_qualifier_matches = FICTIONAL_QUALIFIER_PATTERN.findall(text)
    if fictional_qualifier_matches:
        signals.append(('fictional_qualifier', 1.0,
                        f'Text explicitly uses fictional qualifiers: {", ".join(fictional_qualifier_matches)}'))
        weighted_scores.append(('fictional_qualifier', 1.0, 5.0))  # automatic detection

    # --- Combine scores ---
    if not weighted_scores:
        ai_probability = 0.0
    else:
        total_weight = sum(w for _, _, w in weighted_scores)
        weighted_sum = sum(score * weight for _, score, weight in weighted_scores)
        ai_probability = weighted_sum / total_weight

        # Boost if multiple signals agree (convergent evidence)
        active_signals = sum(1 for _, s, _ in weighted_scores if s > 0.3)
        if active_signals >= 4:
            ai_probability = min(ai_probability * 1.3, 1.0)
        elif active_signals >= 3:
            ai_probability = min(ai_probability * 1.15, 1.0)

    # Clamp
    ai_probability = max(0.0, min(ai_probability, 0.99))

    # Threshold for binary decision
    is_ai_generated = ai_probability >= 0.40

    return {
        'is_ai_generated': is_ai_generated,
        'ai_probability': round(ai_probability, 4),
        'signals': [(name, round(score, 3), desc) for name, score, desc in signals],
        'fictional_entities': fictional_entities,
        'verified_entities': verified_entities,
    }
