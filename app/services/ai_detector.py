def _check_explicit_markers(text_lower):
    """Check for explicit AI / fictional content markers."""
    markers_found = []
    for marker in EXPLICIT_AI_MARKERS:
        if marker in text_lower:
            markers_found.append(marker)

    # Check for "fictional" qualifier near entity names
    if FICTIONAL_QUALIFIER_PATTERN.search(text_lower):
        markers_found.append('fictional/fictitious qualifier used')

    return markers_found


def _analyze_sentence_uniformity(text):
    """
    AI text tends to have very uniform sentence lengths.
    Human text has much more variation.

    Returns a score from 0.0 (high variation = human) to 1.0 (uniform = AI).
    """
    # Split into sentences
    sentences = re.split(r'[.!?]+', text)
    sentences = [s.strip() for s in sentences if len(s.strip()) > 10]

    if len(sentences) < 4:
        return 0.0  # Not enough data

    lengths = [len(s.split()) for s in sentences]
    mean_len = statistics.mean(lengths)
    if mean_len < 5:
        return 0.0

    stdev = statistics.stdev(lengths)
    cv = stdev / mean_len  # Coefficient of variation

    # AI typically has CV < 0.35, humans > 0.50
    if cv < 0.25:
        return 0.85
    elif cv < 0.35:
        return 0.60
    elif cv < 0.45:
        return 0.30
    else:
        return 0.0


def _analyze_transition_density(text_lower):
    """
    Count AI-typical transition phrases per sentence.
    AI text has a much higher density than human writing.

    Returns (score, count).
    """
    sentences = re.split(r'[.!?]+', text_lower)
    sentences = [s.strip() for s in sentences if len(s.strip()) > 10]
    num_sentences = max(len(sentences), 1)

    count = 0
    for phrase in AI_TRANSITION_PHRASES:
        count += text_lower.count(phrase)

    density = count / num_sentences

    if density > 0.5:
        return 0.80, count
    elif density > 0.3:
        return 0.50, count
    elif density > 0.2:
        return 0.25, count
    else:
        return 0.0, count


def _analyze_hedging_balance(text):
    """
    AI loves to present both sides of an argument in a formulaic way.
    E.g., "Some experts argue X... while others contend Y."

    Returns a score from 0.0 to 1.0.
    """
    pair_count = 0
    for pattern_a, pattern_b in AI_HEDGING_PAIRS:
        has_a = re.search(pattern_a, text, re.IGNORECASE)
        has_b = re.search(pattern_b, text, re.IGNORECASE)
        if has_a and has_b:
            pair_count += 1

    if pair_count >= 2:
        return 0.75
    elif pair_count == 1:
        return 0.35
    return 0.0


def _analyze_passive_voice(text):
    """
    AI-generated news tends to use more passive voice and reported speech
    than human journalists.

    Returns (score, count).
    """
    sentences = re.split(r'[.!?]+', text)
    sentences = [s.strip() for s in sentences if len(s.strip()) > 10]
    num_sentences = max(len(sentences), 1)

    passive_count = len(PASSIVE_VOICE_PATTERN.findall(text))
    reported_count = sum(len(p.findall(text)) for p in _compiled_reported)

    total = passive_count + reported_count
    density = total / num_sentences

    if density > 0.8:
        return 0.70, total
    elif density > 0.5:
        return 0.45, total
    elif density > 0.3:
        return 0.20, total
    else:
        return 0.0, total


def _analyze_adverb_density(text_lower):
    """
    AI overuses specific adverbs like "significantly", "potentially", etc.

    Returns (score, count).
    """
    words = text_lower.split()
    total_words = max(len(words), 1)

    count = 0
    for adverb in AI_ADVERBS:
        count += text_lower.count(adverb)

    density = count / total_words

    if density > 0.02:
        return 0.65, count
    elif density > 0.01:
        return 0.35, count
    elif density > 0.005:
        return 0.15, count
    else:
        return 0.0, count


def _analyze_paragraph_regularity(text):
    """
    AI paragraphs tend to be similar in length and follow a consistent
    structure (topic sentence → elaboration → conclusion).

    Returns a score from 0.0 to 1.0.
    """
    paragraphs = [p.strip() for p in text.split('\n') if len(p.strip()) > 20]

    if len(paragraphs) < 3:
        return 0.0

    lengths = [len(p.split()) for p in paragraphs]
    mean_len = statistics.mean(lengths)
    if mean_len < 10:
        return 0.0

    stdev = statistics.stdev(lengths)
    cv = stdev / mean_len

    if cv < 0.20:
        return 0.70
    elif cv < 0.35:
        return 0.40
    elif cv < 0.50:
        return 0.15
    else:
        return 0.0


def _verify_entities_exist(text):
    """
    Extract key entities from the text and verify they exist on the web.
    Fictional entities are a very strong AI-generation signal.

    Returns (fictional_entities, verified_entities).
    """
    entities = _extract_key_entities(text)
    fictional = []
    verified = []

    for entity in entities[:5]:  # Limit to 5 to control latency
        if _verify_entity_exists(entity):
            verified.append(entity)
        else:
            fictional.append(entity)

    return fictional, verified


