"""
AI-Generated Content Detector.

Detects whether text was likely generated by an AI language model (ChatGPT,
Claude, Grok, etc.) using multiple structural and statistical signals:

  1. Fictional / unverifiable entity detection
  2. Sentence length uniformity analysis
  3. Transition phrase density
  4. Passive voice density
  5. Formulaic adverb density
  6. Paragraph structure regularity
  7. Hedging uniformity (balanced "some say X, others Y" patterns)
  8. Web corroboration for key entities (institutions, journals, people)

None of these signals alone is conclusive, but combined they produce a
reliable indicator of AI authorship.
"""

import re
import math
import statistics
try:
    from ddgs import DDGS
except ImportError:
    DDGS = None
from app.services.text_utils import normalize_text, normalize_for_heuristics

# ---------------------------------------------------------------------------
# Signal 1: Explicit AI / Fictional markers in the text itself
# ---------------------------------------------------------------------------
EXPLICIT_AI_MARKERS = [
    'as an ai language model', 'generated by gpt', 'generated by ai',
    'i cannot fulfill', 'as a large language model',
    'this is a fictional', 'fabricated news',
    'in this hypothetical scenario', 'for testing purposes',
    'generated by chatgpt', 'generated by claude', 'generated by grok',
    'written by ai', 'ai-generated', 'created by an ai',
    'as a machine learning model', 'as an artificial intelligence',
    'since i am an ai', 'i am a text-based ai', 'as an automated assistant',
    'not a real person', 'fabricated text', 'simulated scenario',
]

# The word "fictional" appearing near an entity name (e.g. "the fictional
# International Vision Research Institute") is a dead giveaway.
FICTIONAL_QUALIFIER_PATTERN = re.compile(
    r'\b(?:fictional|fictitious|hypothetical|imaginary|made[- ]?up|synthetic|not\s+real|simulated|fabricated|fake)\b',
    re.IGNORECASE,
)

# ---------------------------------------------------------------------------
# Signal 2: Transition / connector phrases that AI overuses
# ---------------------------------------------------------------------------
AI_TRANSITION_PHRASES = [
    'furthermore', 'moreover', 'in addition', 'additionally',
    'consequently', 'as a result', 'in conclusion', 'to summarize',
    'it is worth noting', 'it should be noted', 'notably',
    'on the other hand', 'in contrast', 'conversely',
    'nevertheless', 'nonetheless', 'notwithstanding',
    'in light of', 'with regard to', 'in terms of',
    'it is important to note', 'it is essential to',
    'this highlights', 'this underscores', 'this suggests',
    'moving forward', 'going forward', 'looking ahead',
    'that being said', 'having said that', 'with that in mind',
    'broadly speaking', 'generally speaking',
    'in the broader context', 'from a broader perspective',
]

# ---------------------------------------------------------------------------
# Signal 3: Formulaic hedging phrases AI uses for "balance"
# ---------------------------------------------------------------------------
AI_HEDGING_PAIRS = [
    # (one side, other side) — AI loves to present both sides formulaically
    (r'(?:some|several|many)\s+(?:\w+\s+){0,3}(?:argue|claim|believe|say|suggest|contend)',
     r'(?:others|while others|but others|some others)\s+(?:\w+\s+){0,3}(?:argue|claim|believe|say|suggest|contend|disagree|counter)'),
    (r'(?:proponents|supporters|advocates)\s+(?:of|say|argue|claim|believe)',
     r'(?:critics|opponents|skeptics|detractors)\s+(?:say|argue|claim|believe|counter|warn)'),
    (r'(?:on\s+(?:the\s+)?one\s+hand)',
     r'(?:on\s+the\s+other\s+hand)'),
]

# ---------------------------------------------------------------------------
# Signal 4: Passive voice patterns
# ---------------------------------------------------------------------------
PASSIVE_VOICE_PATTERN = re.compile(
    r'\b(?:is|are|was|were|been|being|be)\s+'
    r'(?:reportedly|said\s+to\s+be|expected\s+to\s+be|believed\s+to\s+be|'
    r'thought\s+to\s+be|considered\s+to\s+be|'
    r'(?:\w+(?:ed|en))\b)',
    re.IGNORECASE,
)

# Specific "reported speech" patterns AI loves
REPORTED_SPEECH_PATTERNS = [
    r'(?:is|are)\s+said\s+to\b',
    r'(?:is|are)\s+expected\s+to\b',
    r'(?:is|are)\s+believed\s+to\b',
    r'(?:is|are)\s+reported\s+to\b',
    r'(?:has|have)\s+reportedly\b',
    r'(?:is|are)\s+reportedly\b',
    r'\breportedly\b',
    r'\ballegedly\b',
    r'\bpurportedly\b',
]
_compiled_reported = [re.compile(p, re.IGNORECASE) for p in REPORTED_SPEECH_PATTERNS]

# ---------------------------------------------------------------------------
# Signal 5: Formulaic adverbs AI overuses
# ---------------------------------------------------------------------------
AI_ADVERBS = [
    'significantly', 'potentially', 'particularly', 'specifically',
    'essentially', 'fundamentally', 'ultimately', 'increasingly',
    'subsequently', 'accordingly', 'presumably', 'reportedly',
    'apparently', 'evidently', 'purportedly', 'ostensibly',
    'arguably', 'undoubtedly', 'inevitably', 'inherently',
    'predominantly', 'substantially', 'considerably', 'remarkably',
]

# ---------------------------------------------------------------------------
# Signal 6: Entity extraction for web corroboration
# ---------------------------------------------------------------------------

# Patterns to extract institution / organization names
INSTITUTION_PATTERN = re.compile(
    r'(?:the\s+)?'
    r'(?:fictional\s+)?'
    r'('
    r'(?:[A-Z][a-zA-Z]+\s+){1,5}'  # 1-5 capitalized words
    r'(?:Institute|University|Foundation|Center|Centre|Laboratory|Lab|'
    r'Organization|Organisation|Society|Association|Agency|Authority|'
    r'Commission|Council|Board|Bureau|Department|Ministry|'
    r'Corporation|Company|Group|Consortium|Alliance|Coalition|Network|'
    r'Hospital|Clinic|Academy|School|College)'
    r')'
    r'(?:\s+\([A-Z]{2,6}\))?',  # optional acronym like (IVRI)
    re.MULTILINE,
)

# Extract journal / publication names (hardened against arbitrary whitespace)
JOURNAL_PATTERN = re.compile(
    r'(?:the\s+)?'
    r'(?:fictional\s+|fake\s+|imaginary\s+)?'
    r'(?:Journal|Bulletin|Review|Proceedings|Annals|Archives|Gazette|'
    r'Quarterly|Monthly|Weekly|Daily|Tribune|Herald|Times|Post|Chronicle)'
    r'[\s\W]+of[\s\W]+'
    r'(?:[A-Z][a-zA-Z]+\s*){1,5}',
    re.MULTILINE,
)

# Extract study/report references
STUDY_PATTERN = re.compile(
    r'(?:a\s+)?(?:new\s+|recent\s+|latest\s+|major\s+|landmark\s+)?'
    r'(?:study|report|analysis|survey|investigation|paper|research)'
    r'(?:\s+(?:released|published|conducted|carried\s+out|led\s+by|from|by))'
    r'(?:\s+(?:today|recently|this\s+(?:week|month|year)))?'
    r'(?:\s+by\s+(?:researchers|scientists|experts|analysts|doctors)\s+'
    r'(?:at|from)\s+(?:the\s+)?)?'
    r'(.{0,100})',
    re.IGNORECASE,
)

# Acronym pattern — extract things like (IVRI), (WHO), etc.
ACRONYM_PATTERN = re.compile(r'\(([A-Z]{2,8})\)')


def _extract_key_entities(text):
    """
    Extract institutions, journals, and acronyms that should be verifiable.
    Returns a list of entity strings to search for.
    """
    entities = []

    # Institutions
    for m in INSTITUTION_PATTERN.finditer(text):
        name = m.group(1).strip()
        if len(name) > 10:  # Filter out very short matches
            entities.append(name)

    # Journals
    for m in JOURNAL_PATTERN.finditer(text):
        name = m.group(0).strip()
        if 'fictional' not in name.lower():
            entities.append(name)
        else:
            # The text literally says "fictional" — dead giveaway
            entities.append(name.replace('fictional ', '').replace('Fictional ', ''))

    # Acronyms — only if they're not common known ones
    COMMON_ACRONYMS = {
        'US', 'UK', 'EU', 'UN', 'WHO', 'NASA', 'FBI', 'CIA', 'BBC', 'CNN',
        'GDP', 'CEO', 'CTO', 'CFO', 'COO', 'PhD', 'MD', 'AP', 'AFP',
        'IMF', 'NATO', 'UNICEF', 'UNESCO', 'ISRO', 'MIT', 'IIT',
        'AIIMS', 'NIH', 'CDC', 'FDA', 'EPA', 'DOJ', 'DOD', 'NSA',
        'FIDE', 'ICC', 'FIFA', 'IOC', 'NBA', 'NFL', 'MLB', 'NHL',
        'IPL', 'BCCI', 'ECB', 'RBI', 'SEBI', 'SEC', 'FTC',
    }
    for m in ACRONYM_PATTERN.finditer(text):
        acr = m.group(1)
        if acr not in COMMON_ACRONYMS:
            entities.append(acr)

    return entities


def _verify_entity_exists(entity):
    """
    Search the web for an entity. Returns True if it appears to be real
    (i.e., has meaningful search results), False if it seems fictional.
    """
    if DDGS is None:
        return True  # Can't verify, assume real to avoid false positives
    try:
        with DDGS() as ddgs:
            results = list(ddgs.text(f'"{entity}"', max_results=3))

        if not results:
            return False

        # Check if any result actually mentions this entity
        entity_lower = entity.lower()
        entity_words = set(entity_lower.split())

        for r in results:
            combined = (r.get('title', '') + ' ' + r.get('body', '')).lower()
            # Check if the entity name or most of its words appear
            matched_words = sum(1 for w in entity_words if w in combined)
            if matched_words >= len(entity_words) * 0.6:
                return True

        return False

    except Exception:
        # On error, assume the entity is real to avoid false positives
        return True


